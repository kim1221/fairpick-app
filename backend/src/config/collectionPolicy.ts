/**
 * KOPIS 과거 버퍼: 오늘 기준 X일 전까지 수집
 * KOPIS API는 stdate(시작일) 기준으로 필터링하므로,
 * 이 값보다 오래 전에 시작한 진행 중인 공연은 날짜 업데이트를 받지 못함.
 *
 * 변경 이력:
 *   7일 → 90일 (2026-03-23): 장기 공연 날짜 업데이트 누락 방지
 *   90일 → 30일 (2026-04-14): 90일 범위가 수집 파이프라인 7시간+ 지연 유발
 *     - 한국 대부분의 공연은 2-4주 단위 → 30일이면 충분
 *     - 오픈런 뮤지컬(30일+ 이전 시작)은 forward window에서 여전히 잡힘
 *     - 60분 per-step 타임아웃과 병행하여 최악 상황 방어
 */
export const KOPIS_DAYS_BACK = 30;

/** KOPIS 미래 범위: 오늘 기준 X일 후까지 수집 (변경 전: 730일) */
export const KOPIS_DAYS_FORWARD = 120;

/** 문화포털 미래 범위: 오늘 기준 X일 후까지 수집 (변경 전: ~365일) */
export const CULTURE_DAYS_FORWARD = 90;

/** TourAPI 과거 버퍼: 오늘 기준 X일 전까지 수집 (변경 전: 2025-01-01 고정) */
export const TOURAPI_DAYS_BACK = 30;

/** TourAPI 미래 범위: 오늘 기준 X일 후까지 수집 (변경 전: 상한 없음) */
export const TOURAPI_DAYS_FORWARD = 180;

